# basic_operators.py

# 算术运算符
a = 10
b = 3

print("=== 算术运算符 ===")
print(f"a = {a}, b = {b}")
print(f"a + b = {a + b}")   # 加法
print(f"a - b = {a - b}")   # 减法
print(f"a * b = {a * b}")   # 乘法
print(f"a / b = {a / b}")   # 除法
print(f"a % b = {a % b}")   # 取余
print(f"a ** b = {a ** b}") # 幂运算
print(f"a // b = {a // b}") # 整除

# 比较运算符
print("\n=== 比较运算符 ===")
print(f"a = {a}, b = {b}")
print(f"a == b : {a == b}")  # 等于
print(f"a != b : {a != b}")  # 不等于
print(f"a > b : {a > b}")    # 大于
print(f"a < b : {a < b}")    # 小于
print(f"a >= b : {a >= b}")  # 大于等于
print(f"a <= b : {a <= b}")  # 小于等于

# 逻辑运算符
print("\n=== 逻辑运算符 ===")
x = True
y = False

print(f"x = {x}, y = {y}")
print(f"x and y : {x and y}")  # 与
print(f"x or y : {x or y}")    # 或
print(f"not x : {not x}")      # 非