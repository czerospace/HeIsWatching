# 变量是 Python 中用于存储数据的“标签”，你可以把它想象成贴在值上的便利贴。
# 创建变量时，Python 会在内存中开辟空间，并把变量名指向这块空间。

# 1. 基本赋值：把整数 10 赋给变量 age
age = 10

# 2. 字符串变量：把文本赋给变量 name
name = "小明"

# 3. 浮点数变量：存储小数
price = 9.9

# 4. 布尔变量：只有 True 或 False 两个取值
is_student = True

# 5. 动态类型：同一个变量可以先后指向不同类型的值
x = 100        # x 现在是整数
x = "hello"    # x 现在变成字符串

# 6. 多重赋值：一行给多个变量赋不同的值
a, b, c = 1, 2, 3

# 7. 交换两个变量的值，无需临时变量
m, n = 7, 8
m, n = n, m

# 8. 查看变量类型
print(type(age))   # <class 'int'>

# 添加测试输出
print("这是测试输出")
print("变量age的值是:", age)
print("变量name的值是:", name)
